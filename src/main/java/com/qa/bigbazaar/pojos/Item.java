package com.qa.bigbazaar.pojos;

public class Item {
    //POJO for items
	
	private String itemName;
	private String itemMRP;
	private String itemCSP;
	private String itemDiscount;
	private String itemQuantity;
	private String itemSize;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemMRP() {
		return itemMRP;
	}
	public void setItemMRP(String itemMRP) {
		this.itemMRP = itemMRP;
	}
	public String getItemCSP() {
		return itemCSP;
	}
	public void setItemCSP(String itemCSP) {
		this.itemCSP = itemCSP;
	}
	public String getItemDiscount() {
		return itemDiscount;
	}
	public void setItemDiscount(String itemDiscount) {
		this.itemDiscount = itemDiscount;
	}
	public String getItemQuantity() {
		return itemQuantity;
	}
	public void setItemQuantity(String itemQuantity) {
		this.itemQuantity = itemQuantity;
	}
	public String getItemSize() {
		return itemSize;
	}
	public void setItemSize(String itemSize) {
		this.itemSize = itemSize;
	}

}
